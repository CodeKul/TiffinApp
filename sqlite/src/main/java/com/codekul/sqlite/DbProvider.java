    @Override
    public Cursor query(Uri uri, String[] projection, String selection,
                        String[] selectionArgs, String sortOrder) {
        App app = (App) getContext();
        Log.i("@codekul","App "+app);
        Log.i("@codekul","Helper "+app.helper());

        SQLiteDatabase sqDb = app.helper().getReadableDatabase();
        Cursor cursor = sqDb.query(Db.Mobile.TAB_NAME, projection, selection, selectionArgs, null, null, sortOrder);

        return cursor;
    }

